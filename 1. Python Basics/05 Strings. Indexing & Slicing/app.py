greeting = 'Hello Python'

greeting_length = len(greeting)
print(greeting_length)
print(len(greeting))

# Indexing
print(greeting[0])
print(greeting[6])

print('Last symbol: ' + greeting[-1])
print(greeting[-12])

# Slicing
print(greeting[2:5])
print(greeting[6:10])
print(greeting[-5:-2])

print(greeting[2:])
print(greeting[:5])

print(greeting[:])

print(greeting[1:10:2])
print(greeting[::2])

print(greeting[1:9:3])

print(greeting[::-1])
